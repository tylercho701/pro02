package com.daiso.dto;

public class Category {
	private String catecode;
	private String categroup;
	private String catename;
	
	public final String getCatecode() {
		return catecode;
	}
	public final void setCatecode(String catecode) {
		this.catecode = catecode;
	}
	public final String getCategroup() {
		return categroup;
	}
	public final void setCategroup(String categroup) {
		this.categroup = categroup;
	}
	public final String getCatename() {
		return catename;
	}
	public final void setCatename(String catename) {
		this.catename = catename;
	}
}